package utils;

public class IrregularPentagon {
    private double edge1;
    private double edge2;
    private double edge3;
    private double edge4;
    private double edge5;

    public IrregularPentagon(double edge1, double edge2, double edge3, double edge4, double edge5) {
        this.edge1 = edge1;
        this.edge2 = edge2;
        this.edge3 = edge3;
        this.edge4 = edge4;
        this.edge5 = edge5;
    }

    public double getEdge1() {
        return this.edge1;
    }

    public void setEdge1(double edge1) {
        this.edge1 = edge1;
    }

    public double getEdge2() {
        return this.edge2;
    }

    public void setEdge2(double edge2) {
        this.edge2 = edge2;
    }

    public double getEdge3() {
        return this.edge3;
    }

    public void setEdge3(double edge3) {
        this.edge3 = edge3;
    }

    public double getEdge4() {
        return this.edge4;
    }

    public void setEdge4(double edge4) {
        this.edge4 = edge4;
    }

    public double getEdge5() {
        return this.edge5;
    }

    public void setEdge5(double edge5) {
        this.edge5 = edge5;
    }
}